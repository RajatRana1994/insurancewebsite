                                <h2 class="section-title">What Does Builder's Risk Insurance Cover?</h2>
                                <h2 class="section-title">What Does It Not Cover?</h2>
                            </div>
                            <p>Builder's Risk Insurance generally covers the owner, the lending institution, the contractor(s) performing the work, and other related parties. In addition to providing coverage for damage to the building itself, most plans also cover temporary constructs (like scaffolding), materials, and equipment being used (whether it's currently on-site or off-site). The majority of policies are written on an all-perils basis, which means that reimbursements are available for any losses except those specifically excluded.</p>
                            <p>Common exclusions from this coverage include (but are not limited to):</p>

                            <div class="card list-card">
                                <ul class="list-unstyled common-list">
                                    <li>The value of the land itself</li>
                                    <li> Employee theft</li>
                                    <li>Damages beyond the estimated value of the construction</li>
                                    <li>Any losses brought by law or ordinance changes</li>
                                    <li>Damage from earthquakes</li>
                                    <li>Losses from war, nuclear hazards, or actions by civil authority</li>
                                </ul>
                            </div>
                            <p>As you can see, most of the exclusions are not likely to occur during the course of a normal construction project. The value of the land is the only one likely to change as new work is done, and you will likely need to account for any improvements or losses the next time your company prepares taxes.</p>
