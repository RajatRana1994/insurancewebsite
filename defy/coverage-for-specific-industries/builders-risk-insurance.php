                            <p>Existing buildings are exposed to a wide variety of threats—fire, flooding, and vandalism are just a few of the ways building structures can be harmed. However, buildings under construction aren't ready to face risks that completed buildings can withstand. There's also the chance of an accident during the construction phase, like materials being destroyed or installed incorrectly. A proper risk analysis from our Defy Insurance agents can help reduce the number of threats a new construction faces, and our agents can help reveal additional threats that may need to be covered by additional policies.</p>
                            <p>Builder's risk insurance is a policy designed to mitigate the chance of loss and protect you in the event that damage is done to the building. This policy is most commonly purchased by landowners and those funding the project, although some contractors purchase it in order to have more control over how each job is insured.</p>
                            <p>This policy is mainly designed for new construction, rather than changes and improvements to existing buildings. That form of construction is usually covered by property insurance and does not need any additional coverage, although it's best to talk with an agent before starting any particularly large project just to be absolutely sure it's covered.</p>
